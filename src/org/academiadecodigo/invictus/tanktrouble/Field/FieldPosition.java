package org.academiadecodigo.invictus.tanktrouble.Field;


public class FieldPosition {

    private int x;
    private int y;


    public FieldPosition(int x, int y) {

        this.x = x;
        this.y = y;
    }





    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

}



